#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	ln;

	if (s != 0)
	{
		i = 0;
		ln = ft_strlen(s);
		while (i <= ln)
		{
			s[i] = '\0';
			i++;
		}
	}
}
