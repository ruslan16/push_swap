#include "libft.h"

size_t	ft_wdc(char const *s, char c)
{
	size_t	wdc;
	size_t	i;

	wdc = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && s[i])
			wdc++;
		while (s[i] != c && s[i + 1] != '\0')
			i++;
		i++;
	}
	return (wdc);
}
