#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	i;
	size_t	ln;

	if (s != 0 && f != 0)
	{
		i = 0;
		ln = ft_strlen(s);
		while (i < ln)
		{
			(*f)(&s[i]);
			i++;
		}
	}
}
